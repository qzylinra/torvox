use libghostty_vt::key::Key;

/// Map Android `KeyEvent` key codes to ghostty `key::Key` values.
/// Reference: <https://developer.android.com/reference/android/view/KeyEvent>
pub(crate) fn map_android_key_code(key_code: u32) -> Key {
    match key_code {
        // Alphabet keys
        29 => Key::A,
        30 => Key::B,
        31 => Key::C,
        32 => Key::D,
        33 => Key::E,
        34 => Key::F,
        35 => Key::G,
        36 => Key::H,
        37 => Key::I,
        38 => Key::J,
        39 => Key::K,
        40 => Key::L,
        41 => Key::M,
        42 => Key::N,
        43 => Key::O,
        44 => Key::P,
        45 => Key::Q,
        46 => Key::R,
        47 => Key::S,
        48 => Key::T,
        49 => Key::U,
        50 => Key::V,
        51 => Key::W,
        52 => Key::X,
        53 => Key::Y,
        54 => Key::Z,
        // Digit keys
        7 => Key::Digit0,
        8 => Key::Digit1,
        9 => Key::Digit2,
        10 => Key::Digit3,
        11 => Key::Digit4,
        12 => Key::Digit5,
        13 => Key::Digit6,
        14 => Key::Digit7,
        15 => Key::Digit8,
        16 => Key::Digit9,
        // Symbol keys
        68 => Key::Backquote,
        69 => Key::Minus,
        70 => Key::Equal,
        71 => Key::BracketLeft,
        72 => Key::BracketRight,
        73 => Key::Backslash,
        74 => Key::Semicolon,
        75 => Key::Quote,
        76 => Key::Slash,
        55 => Key::Comma,
        56 => Key::Period,
        // Navigation and editing
        19 => Key::ArrowUp,
        20 => Key::ArrowDown,
        21 => Key::ArrowLeft,
        22 => Key::ArrowRight,
        66 => Key::Enter,
        67 => Key::Backspace,
        112 => Key::Delete,
        61 => Key::Tab,
        62 => Key::Space,
        111 => Key::Escape,
        122 => Key::Home,
        123 => Key::End,
        92 => Key::PageUp,
        93 => Key::PageDown,
        124 => Key::Insert,
        // Modifier keys
        57 => Key::AltLeft,
        58 => Key::AltRight,
        59 => Key::ShiftLeft,
        60 => Key::ShiftRight,
        113 => Key::ControlLeft,
        114 => Key::ControlRight,
        115 => Key::CapsLock,
        116 => Key::ScrollLock,
        143 => Key::NumLock,
        119 => Key::Fn,
        // Function keys
        131 => Key::F1,
        132 => Key::F2,
        133 => Key::F3,
        134 => Key::F4,
        135 => Key::F5,
        136 => Key::F6,
        137 => Key::F7,
        138 => Key::F8,
        139 => Key::F9,
        140 => Key::F10,
        141 => Key::F11,
        142 => Key::F12,
        // System keys
        117 => Key::MetaLeft,
        118 => Key::MetaRight,
        120 => Key::PrintScreen,
        121 => Key::Pause,
        // Numpad keys
        144 => Key::Numpad0,
        145 => Key::Numpad1,
        146 => Key::Numpad2,
        147 => Key::Numpad3,
        148 => Key::Numpad4,
        149 => Key::Numpad5,
        150 => Key::Numpad6,
        151 => Key::Numpad7,
        152 => Key::Numpad8,
        153 => Key::Numpad9,
        154 => Key::NumpadDivide,
        155 => Key::NumpadMultiply,
        156 => Key::NumpadSubtract,
        157 => Key::NumpadAdd,
        158 => Key::NumpadDecimal,
        159 => Key::NumpadComma,
        160 => Key::NumpadEnter,
        161 => Key::NumpadEqual,
        // Media keys
        85 => Key::MediaPlayPause,
        86 => Key::MediaStop,
        87 => Key::MediaTrackNext,
        88 => Key::MediaTrackPrevious,
        _ => Key::Unidentified,
    }
}
